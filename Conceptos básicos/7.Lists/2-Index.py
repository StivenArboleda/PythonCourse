#Postive
days = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]

print("El valor que se encuentra en el indice 0 es:", days[0])
print("Los valores que se encuentra en el indice 0 hasta 3 es:", days[:3]) #Does not include the 3

#Negative
days = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]

print("El valor que se encuentra en la última posición es:", days[-1])
print("Los valores que se encuentra en el indice 3 hasta el último es:", days[-3:])