# DATATYPE: Integer
entero = 25
print(entero)

# DATATYPE: Float - Flotante
pi = 3.14
print(pi)

#Boolean - lógico - True or false
bandera = True
print(bandera)
# Directly print the value
print(True)
print(False)
# Another way
print(bool(1)) # True
print(bool(0)) #False

# DATATYPE: String 
saludo = "Hola mundo"   #Double quotation marks
saludo2 = 'Hola mundo 2'  #Single quotes
pi = "3.14"             #Numerically
print(saludo)
print(saludo2)
print(pi)

# DATATYPE: Assignment "="

z = 40
z = z + 22

# Finally. Print the data type of the variables
print(type(entero))
print(type(pi))
print(type(bandera))
print(type(saludo))